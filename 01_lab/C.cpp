#include <bits/stdc++.h>
using namespace std;
int main()
{
    int k, i = 3, res = 0;
    cin>>k;
    while(k>0)
    {
        res += k%2 * (1<<i--);
        k/=2;
    }
    cout<<res;
}

// 8 - 1000 : 0001 - 1
// 9 - 1001 : 1001 - 9
// 10 - 1010 : 0101 - 5
// 11 - 1011 : 1101 - 13
// 12 - 1100 : 0011 - 3
// 13 - 1101 : 1011 - 11
// 14 - 1110 : 0111 - 7
// 15 - 1111 : 1111 - 15